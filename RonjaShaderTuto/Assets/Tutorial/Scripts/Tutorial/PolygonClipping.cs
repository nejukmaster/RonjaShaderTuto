using System.Collections;
using System.Collections.Generic;
using UnityEngine;

//????? ??????? ????? ???.
[ExecuteInEditMode]
//??? ????? ????? ???? ??????.
[RequireComponent(typeof(Renderer))]
public class PolygonClipping : MonoBehaviour
{
    [SerializeField] private Vector2[] corners;
    private Material _mat;

    void UpdateMaterial()
    {
        //????? Rederer?? ??
        if (_mat == null)
            _mat = GetComponent<Renderer>().sharedMaterial;

        //1000?? Vector4? ??? ? ?? ??? ??
        Vector4[] vec4Corners = new Vector4[1000];
        for (int i = 0; i < corners.Length; i++)
        {
            //?????? ??? corners? ?? ??? ??? ???? ??
            vec4Corners[i] = corners[i];
        }

        //??? ??? ?? ?????.
        //SetVectorArray(variable name, value)? ??? ?? ??? ??? ?? ?????.
        _mat.SetVectorArray("_corners", vec4Corners);
        //SetInt(variable name, value)? ??? ??? ??? ?? ?????.
        _mat.SetInt("_cornerCount", corners.Length);
    }
    // Start is called before the first frame update
    void Start()
    {
        UpdateMaterial();
    }

    // Update is called once per frame
    void Update()
    {
        UpdateMaterial();
    }
}
